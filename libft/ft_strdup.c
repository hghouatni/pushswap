#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		src_len;
	char	*cpy;

	i = 0;
	src_len = ft_strlen((const char *)src);
	cpy = (char *)malloc(src_len + 1);
	if (!cpy)
		return (NULL);
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
